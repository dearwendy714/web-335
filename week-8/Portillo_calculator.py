# function that takes two parameters(numbers) and adds them together


def add(x, y):
    return x + y

# function that takes two parameters(numbers) and finds the difference


def subtract(x, y):
    return x - y

# function that takes two parameters(numbers) and finds the quotient of them


def divide(x, y):
    return x / y


print(add(10, 10))

print(subtract(10, 10))

print(divide(10, 10))
